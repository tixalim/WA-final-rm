            <strong>Co je to solarpunk</strong><br>
            Zatímco jiné žánry říkají “tohle se může stát pokud nic nezměníme”, solarpunk říká “tohle může být budoucnost, pokud začneme jednat”. 
            Solarpunk je kulturní a estetický směr, který kombinuje udržitelný způsob života a pokročilé technologie s pozitivní vizí budoucnosti. 
            Vznikl jako protiváha k temným a dystopickým sci-fi žánrům, jako je cyberpunk. Na místo varování před technologií klade důraz na její využívání k řešení klimatické krize, 
            sociálních nerovností a krize důvěry ve společnost. Solarpunk spojuje ekologickou architekturu, obnovitelné zdroje energie a komunitní hodnoty a 
            to nejen jako fikci ale i jako filozofii, podle které se lidé snaží žít. Velmi dobrým protikladem je již zmíněný cyberpunk nebo steampunk. 
            Ty ukazují svět ovládaný korporacemi, chudobu a korupci nebo také znovu velkovyužití parních strojů. Zatímco solarpunk představuje budoucnost, 
            kde technologie slouží lidem ale hlavně funguje v harmonii s přírodou. Typickými znaky jsou města pokrytá zelení, domy se solárními panely a vertikálními zahradami. 
            Design je často inspirován organickými tvary, přírodními materiály a světlem, estetika je klidná, teplá a přívětivá. Kromě vizuální stránky má solarpunk i 
            silný ideový základ. Usiluje o decentralizaci moci, soběstačnost komunit a “pomalejší” životní styl, který klade důraz na spolupráci místo soutěživosti. 
            Je to vize budoucnosti ke lidské potřeby a technologie nejsou v rozporu s planetou, ale tvoří jeden propojený celek. V tomto směru je solarpunk víc než žánr, 
            je to nástroj naděje a motivace pro generaci, která čelí nejisté budoucnosti. V posledních letech se koncept solarpunku dostává i do reálných projektů. 
            Architekti, urbanisté a ekologové navrhují města inspirovana tímto směrem. Komunity zakládají zahrady, využívají obnovitelné zdroje energie nebo se učí 
            soběstačnosti. Na rozdíl os pouhé kritiky systému, nabízí solarpunk praktické alternativy, které lze začít praktikovat už dnes. 
